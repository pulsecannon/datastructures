"""Unittest for valid sudoku."""

import unittest

import valid_sudoku

class ValidSudokuTest(unittest.TestCase):

    def test_invalid_grid(self):
        matrix = [["5","3","8",".","7",".",".",".","."]
                  ,["6",".",".","1","9","5",".",".","."]
                  ,[".","9","8",".",".",".",".","6","."]
                  ,["8",".",".",".","6",".",".",".","3"]
                  ,["4",".",".","8",".","3",".",".","1"]
                  ,["7",".",".",".","2",".",".",".","6"]
                  ,[".","6",".",".",".",".","2","8","."]
                  ,[".",".",".","4","1","9",".",".","5"]
                  ,[".",".",".",".","8",".",".","7","9"]]
        self.assertFalse(valid_sudoku.is_valid_sudoku(matrix))

    def test_invalid_row(self):
        matrix = [["5","3",".",".","7",".",".","5","."]
                  ,["6",".",".","1","9","5",".",".","."]
                  ,[".","9","8",".",".",".",".","6","."]
                  ,["8",".",".",".","6",".",".",".","3"]
                  ,["4",".",".","8",".","3",".",".","1"]
                  ,["7",".",".",".","2",".",".",".","6"]
                  ,[".","6",".",".",".",".","2","8","."]
                  ,[".",".",".","4","1","9",".",".","5"]
                  ,[".",".",".",".","8",".",".","7","9"]]
        self.assertFalse(valid_sudoku.is_valid_sudoku(matrix))

    def test_invalid_col(self):
        matrix = [["5","3",".",".","7",".",".",".","."]
                  ,["6",".",".","1","9","5",".",".","."]
                  ,[".","9","8",".",".",".",".","6","."]
                  ,["8",".",".",".","6",".","2",".","3"]
                  ,["4",".",".","8",".","3",".",".","1"]
                  ,["7",".",".",".","2",".",".",".","6"]
                  ,[".","6",".",".",".",".","2","8","."]
                  ,[".",".",".","4","1","9",".",".","5"]
                  ,[".",".",".",".","8",".",".","7","9"]]
        self.assertFalse(valid_sudoku.is_valid_sudoku(matrix))

    def test_valid(self):
        matrix = [["5","3",".",".","7",".",".",".","."],
                  ["6",".",".","1","9","5",".",".","."],
                  [".","9","8",".",".",".",".","6","."],
                  ["8",".",".",".","6",".",".",".","3"],
                  ["4",".",".","8",".","3",".",".","1"],
                  ["7",".",".",".","2",".",".",".","6"],
                  [".","6",".",".",".",".","2","8","."],
                  [".",".",".","4","1","9",".",".","5"],
                  [".",".",".",".","8",".",".","7","9"]]
        self.assertTrue(valid_sudoku.is_valid_sudoku(matrix))


if __name__ == '__main__':
    unittest.main()
